class Employee {
  final String id;
  final String name;
  final String location;
  final String currentAddress;
  final String createdBy;
  final String mode;
  final int moldNumber;

  Employee({
    required this.id,
    required this.name,
    required this.location,
    required this.currentAddress,
    required this.createdBy,
    required this.mode,
    required this.moldNumber,
  });
}
